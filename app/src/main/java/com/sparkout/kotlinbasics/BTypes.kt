class BTypes {

    /*
    `BTypes` supported in kotlin
    */
    fun types() {
        //represents a number type in kotlin
        var byte: Byte = 0
        var short: Short = 0
        var int: Int = 0
        var long: Long = 0L
        var float: Float = 0f
        var double: Double = 0.0

        //represents a string type in kotlin
        var string: String = "Sparkout"
        var char: Char = 'S'

        //represents a boolean type in kotlin
        var boolean: Boolean = true

        println(byte)
        println(short)
        println(int)
        println(long)
        println(float)
        println(double)
        println(string)
        println(char)
        println(boolean)
    }
}